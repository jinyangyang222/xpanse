package org.eclipse.xpanse.modules.deployment.deployers.terraform.terraformboot.api;

import java.util.Optional;
import org.springframework.beans.factory.annotation.Autowired;
import org.springframework.stereotype.Controller;
import org.springframework.web.bind.annotation.RequestMapping;
import org.springframework.web.context.request.NativeWebRequest;

@Controller
@RequestMapping("${openapi.openAPIDefinition.base-path:}")
public class TerraformBootApiController implements TerraformBootApi {

    private final NativeWebRequest request;

    @Autowired
    public TerraformBootApiController(NativeWebRequest request) {
        this.request = request;
    }

    @Override
    public Optional<NativeWebRequest> getRequest() {
        return Optional.ofNullable(request);
    }

}
